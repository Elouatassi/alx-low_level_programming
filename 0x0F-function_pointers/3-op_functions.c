#include "3-calc.h"

/**
 * op_add - Adds two integers
 * @a: First integer
 * @b: second integer
 * Return: Sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtracts two integers
 * @a: First integer
 * @b: second integer
 * Return: Difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two integers
 * @a: First integer
 * @b: second integer
 * Return: Product
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: First integer
 * @b: second integer
 * Return: Quotient
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two integers
 * @a: First integer
 * @b: second integer
 * Return: Remainder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
